// Escreva um programa para calcular e mostrar a soma de dois números

public class Somando
{
    public static void main(String[] args) {
       
       // Declaração de variáveis
       int num1, num2, soma;
       
       // Entrada de dados
       num1 = 7;
       num2 = 3;
       
       // Processamento
       soma = num1 + num2;
       
       // Saída de dados
       System.out.println(soma);
       
    }
}